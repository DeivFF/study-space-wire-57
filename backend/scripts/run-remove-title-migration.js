import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import pool from '../src/config/database.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function runMigration() {
  try {
    console.log('üîÑ Running migration to remove title column from exercises...');
    
    // Read migration file
    const migrationPath = path.join(__dirname, '../migrations/042_remove_exercise_title_column.sql');
    const migrationSQL = fs.readFileSync(migrationPath, 'utf8');
    
    // Execute migration
    await pool.query(migrationSQL);
    
    console.log('‚úÖ Migration completed successfully!');
    console.log('üìù Title column has been removed from lesson_exercises table');
    
  } catch (error) {
    console.error('‚ùå Migration failed:', error);
    process.exit(1);
  } finally {
    await pool.end();
  }
}

runMigration();